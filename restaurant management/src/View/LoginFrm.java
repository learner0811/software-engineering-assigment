    private void LoginClicked(){
        //varible
        String userName = txtUserName.getText();
        String passWord = new String(txtPassWord.getPassword());
        
        
        //check usernameField is blank
        if (userName.equals("")){
            JOptionPane.showMessageDialog(this, "Ban chua nhap tai khoan", "", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        //check passWordField is blank
        if (passWord.equals("")){
            JOptionPane.showMessageDialog(this, "Ban chua nhap mat khau");
            return;
        }
        
        LoginCtr loginCtr = new LoginCtr();
        
        //check if userName or password is incorect
        if (!loginCtr.checkLogin(userName,passWord)){
            JOptionPane.showMessageDialog(this, "Tai khoan hoac mat khau khong dung", "", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        JOptionPane.showMessageDialog(this, "Dang nhap thanh cong");
        this.setVisible(false);
        java.awt.EventQueue.invokeLater(new Runnable() {
            public void run() {
                new MenuManagementFrm().setVisible(true);
            }
        });
        
    }
    
